import logging
from typing import List

# Настраиваем логгер для отслеживания процесса нарезки батчей
logger = logging.getLogger(__name__)

class DataBatcher:
    """
    Класс для интеллектуального разбиения массива текстов (постов) на батчи (пакеты).

    Разбиение происходит ИСКЛЮЧИТЕЛЬНО на основе суммарной длины текста в символах.
    Количество постов в одном батче не ограничено — их может быть 2, а может быть 100,
    главное, чтобы их общая длина не превышала заданный лимит.
    """

    def __init__(self, max_chars_per_batch):
        """
        Инициализация батчера.

        :param max_chars_per_batch: Максимально допустимая суммарная длина всех строк
                                    в одном батче (в символах). По умолчанию 4000.
        """
        self.max_chars_per_batch = max_chars_per_batch

    def create_batches(self, data: List[str]) -> List[List[str]]:
        """
        Разбивает плоский список строк на список батчей (список списков).

        Логика работы (Алгоритм "Жадного" сбора):
        1. Итерируемся по списку постов.
        2. Проверяем, влезет ли текущий пост в собираемый батч по лимиту символов.
        3. Если не влезает — закрываем текущий батч, сохраняем его и начинаем новый.
        4. Если один пост сам по себе больше лимита, он выделяется в отдельный батч
           (с предупреждением в логи), чтобы не потерять данные.

        :param data: Исходный список всех постов (строк).
        :return: Список батчей. Каждый батч — это List[str].
        """
        # Если передали пустой список данных, возвращаем пустой список батчей
        if not data:
            return []

        batches: List[List[str]] = []  # Здесь будем хранить итоговые батчи
        current_batch: List[str] = []  # Временное хранилище для постов текущего батча
        current_chars: int = 0  # Счетчик символов в текущем батче

        for item in data:
            item_len = len(item)

            # --- Краевой случай: Пост-гигант ---
            # Если длина одного поста превышает общий лимит на батч
            if item_len > self.max_chars_per_batch:
                logger.warning(
                    f"Внимание: найден пост длиной {item_len} символов. "
                    f"Это превышает лимит батча ({self.max_chars_per_batch}). "
                    f"Он будет помещен в отдельный независимый батч."
                )

            # --- Проверка лимитов ---
            # Если в текущем батче уже есть посты, и добавление нового поста
            # приведет к превышению лимита max_chars_per_batch:
            if current_batch and (current_chars + item_len > self.max_chars_per_batch):
                # 1. Сохраняем собранный батч в итоговый список
                batches.append(current_batch)
                # 2. Очищаем временные переменные для сборки следующего батча
                current_batch = []
                current_chars = 0

            # --- Добавление поста ---
            # Добавляем пост в текущий (возможно, только что очищенный) батч
            current_batch.append(item)
            current_chars += item_len

        # --- Сохранение остатков ---
        # После завершения цикла проверяем, не остался ли "хвост" (непустой батч).
        # Если остался — обязательно добавляем его в итоговый список
        if current_batch:
            batches.append(current_batch)

        return batches